package it.polimi.ingsw.ps21.view;

public class InitialSettingsData {
	private String name;
	private int connectionMethod;
	private boolean wantsAdvRules;
	
	public InitialSettingsData(String name, int connectionMethod, boolean wantsAdvRules) {
		super();
		this.name = name;
		this.connectionMethod = connectionMethod;
		this.wantsAdvRules = wantsAdvRules;
	}
	
	
}
